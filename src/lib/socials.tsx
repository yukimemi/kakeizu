export function SocialIcon({
  service,
  size = 18,
  className,
}: {
  service: SocialService;
  size?: number;
  className?: string;
}) {
  const def = SOCIAL_SERVICES.find((s) => s.id === service);
  if (!def) return null;
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={def.color}
      aria-hidden
      className={className}
    >
      <path d={def.iconPath} />
    </svg>
  );
}

export function buildSocialUrl(
  service: SocialService,
  handle: string,
): string | null {
  const def = SOCIAL_SERVICES.find((s) => s.id === service);
  if (!def) return null;
  return def.toUrl(handle);
}
